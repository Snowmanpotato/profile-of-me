function goUp(){
    let moveUp = document.getElementById("nobita");
    moveUp.style.top = parseInt(moveUp.style.top) -5 + "px";
}
function goDown(){
    let moveDown = document.getElementById("nobita");
    moveUp.style.top = parseInt(moveUp.style.top) +5 + "px";
}
function goLeft(){
    let moveUp = document.getElementById("nobita");
    moveUp.style.left = parseInt(moveUp.style.left) -5 + "px";
}
function goRight(){
    let moveUp = document.getElementById("nobita");
    moveUp.style.left = parseInt(moveUp.style.left) +5 + "px";
}